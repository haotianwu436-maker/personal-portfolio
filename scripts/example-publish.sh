#!/bin/bash

# 示例：自动发布第一篇文章
# 这个脚本展示了如何使用publish-article.sh脚本

set -e

echo "📝 示例：发布第一篇文章"
echo "================================"
echo ""

# 文章信息
TITLE="为什么建立这个网站"
SLUG="why-build-this-website"
EXCERPT="半梦半醒，永远年轻，永远热泪盈眶。这句话一直是我内心的写照。在这个快速变化的世界里，我希望能有一个地方，可以慢下来，记录下那些真正重要的东西。"
TAGS="网站,初心,理念"

# 文章内容（Markdown格式）
read -r -d '' CONTENT << 'EOF' || true
# 为什么建立这个网站

## 初心

半梦半醒，永远年轻，永远热泪盈眶。

这句话一直是我内心的写照。在这个快速变化的世界里，我希望能有一个地方，可以慢下来，记录下那些真正重要的东西。

## 理念

### 社区的力量

我相信社区的力量。一个人的力量是有限的，但一群志同道合的人可以改变世界。这个网站就是我想建立的一个社区——一个可以分享想法、交流经验、互相学习的地方。

### 文化的温度

在快速发展的技术世界中，我们有时会忽略人文的温度。我想通过这个网站，展现技术背后的人性，分享那些关于文化、社区、人生的思考。

### 长期主义

我不相信快速成功。我相信长期主义——慢慢积累，持续输出，最终形成自己独特的声音和价值。这个网站就是我践行长期主义的一个载体。

## 为什么是现在

技术在进步，工具在变化，但人的思考和感受是永恒的。现在是一个好时机，去记录下我对这个世界的理解，去分享我的想法，去连接那些志同道合的人。

## 结语

这个网站不会是完美的，但它会是真实的。我会在这里分享我的思考、我的实践、我的故事。

Built slowly, with care.
EOF

echo "📋 文章信息："
echo "  标题：$TITLE"
echo "  Slug：$SLUG"
echo "  标签：$TAGS"
echo ""

# 运行发布脚本
echo "🚀 开始发布文章..."
echo ""

bash ./scripts/publish-article.sh "$TITLE" "$SLUG" "$EXCERPT" "$CONTENT" "$TAGS"

echo ""
echo "✅ 文章发布完成！"
echo ""
echo "📋 下一步："
echo "1. 在Manus UI中打开Dashboard"
echo "2. 找到最新的checkpoint"
echo "3. 点击'Publish'按钮发布网站"
echo "4. 访问 https://persfolio-iet8jqpk.manus.space/blog 查看文章"
echo ""
